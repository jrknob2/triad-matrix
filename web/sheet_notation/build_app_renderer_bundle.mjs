import { readFile, writeFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const directory = dirname(fileURLToPath(import.meta.url));
const sources = [
  'duration.js',
  'voice_mapping.js',
  'document.js',
  'renderer.js',
];

const chunks = [];
for (const source of sources) {
  const path = join(directory, source);
  const contents = await readFile(path, 'utf8');
  chunks.push(
    `// Source: ${source}\n${toPlainScript(contents)}`,
  );
}

await writeFile(
  join(directory, 'app_renderer.js'),
  `// Generated by build_app_renderer_bundle.mjs. Do not edit by hand.\n(() => {\n'use strict';\n${chunks.join('\n\n')}\n})();\n`,
);

function toPlainScript(contents) {
  return contents
    .replace(/^import .+;\n/gm, '')
    .replace(/^export const /gm, 'const ')
    .replace(/^export function /gm, 'function ');
}
